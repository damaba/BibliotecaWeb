package modelo;

/**
 *
 * @author David
 */
public class Libro {
    public int id;
    public String titulo;
    public int paginas;
    public String autor;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public int getPaginas() {
        return paginas;
    }

    public void setPaginas(int paginas) {
        this.paginas = paginas;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    @Override
    public String toString() {
        return "libro{" + "titulo=" + titulo + ", paginas=" + paginas + ", autor=" + autor + '}';
    }
    
    
}
